package main.lesson_01;

public class lab_01_Variable_Learning {
    public static void main(String[] args) {
        byte byteNumber = 1;
        short shortNumber = 128;
        int integerNumber = 129;
        float floatNumber = 2.7f;
        long longNumber = 456254L;
        double doubleNumber = 2.1D;
        char c = 'a';
        String name = "Pham Phan Nhat Khanh";

        boolean myBVar = true;
        myBVar = false;

        System.out.println("Max value of Byte : "+ Byte.MAX_VALUE);
        System.out.println("Min value of Byte : "+Byte.MIN_VALUE);
        System.out.println("Max value of Short : "+Short.MAX_VALUE);
        System.out.println("Min value of Byte : "+Short.MIN_VALUE);
    }
}
